     public void displayDatasWithinTable() {
        bookSummaryService = new BookSummaryService();
        List<BookSummary> pList = bookSummaryService.getList();
        DefaultTableModel model = (DefaultTableModel) tblDisplay.getModel();
        Object[] row = new Object[6];

        for (int i = 0; i < pList.size(); i++) {
            row[0] = pList.get(i).getId();
            row[1] = pList.get(i).getBookCode();
            row[2] = pList.get(i).getBookName();
            row[3] = pList.get(i).getTotalStock();
            row[4] = pList.get(i).getAvilableStock();
            row[5] = pList.get(i).getLostQty();
            model.addRow(row);
        }

    }
